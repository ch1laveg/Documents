    private void jPasswordField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField1FocusLost
        char[] pwd = jPasswordField1.getPassword();
        String password = "";
        for (int i = 0; i < pwd.length; i++){
            password = password + pwd[i];
        }  
        if (password.equals("")){
            jPasswordField1.setText("Пароль");
        }        
    }//GEN-LAST:event_jPasswordField1FocusLost

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        char[] pwd = jPasswordField1.getPassword();
        String password = "";
        for (int i = 0; i < pwd.length; i++){
            password = password + pwd[i];            
        }
        String name = jTextField1.getText();
        String[] user = new String[3];
        user = DBW.getUser(name);
        try{
            if (user[0].equals(jTextField1.getText()) && user[1].equals(password)){
                this.setVisible(false);
                WF.setVisible(true);
                Thread t = new Thread(new Refresher(WF));
                t.start();
            }
        }
        catch(NullPointerException e){
            System.out.println("Неверный логин или пароль");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

